# -*- coding: utf-8 -*-
import re, sys, json

def clean(s):
    # strip HTML tags and entities
    s = re.sub(r"<[^>]+>", "", s)
    s = s.replace("&thinsp;", " ").replace("&nbsp;", " ")
    # qere/ktiv: keep bracketed qere [..], drop parenthesized ktiv (..)
    s = re.sub(r"\([^)]*\)", "", s)
    s = s.replace("[","").replace("]","")
    # strip {פ}{ס} section markers
    s = re.sub(r"\{[פס]\}", "", s)
    # remove ta'amim/accents (U+0591-U+05AF), meteg (U+05BD), paseq (U+05C0), sof-pasuq (U+05C3), nun-hafucha etc.
    s = re.sub(r"[֑-ֽֿ֯׀׃ׅׄ׆]", "", s)
    # divine name -> double-yud   (yod, opt sheva, heh, opt holam, vav, qamats, heh)
    s = re.sub("יְ?הֹ?וָה", "יְיָ", s)
    # tidy whitespace
    s = re.sub(r"[ \t]+", " ", s).strip()
    return s

